	ORG 00H
	AJMP INIT
	ORG 50H

CHAR3:
	DB 024H
	DB 014H
	DB 0FH
	DB 014H
	DB 024H

CHAR2:
	DB 01CH
	DB 014H
	DB 07FH
	DB 014H
	DB 01CH

CHAR1:
	DB 04H
	DB 020H
	DB 07FH
	DB 00H
	DB 04H

DISPLAYSHIFTER:
	DB 010H
	DB 08H
	DB 04H
	DB 02H
	DB 01H

INIT:
	MOV TMOD, #01H 

LOOP:
	MOV R0, #03CH
	MOV R1, #03H
	ACALL DISPLAYFUNC
	JMP LOOP

DISPLAYFUNC:

DISPLAYINIT:
	MOV R2, #05H

DISPLAYCHAR3:
	CJNE R1, #03H, DISPLAYCHAR2
	MOV DPTR, #CHAR3
	JMP DISPLAY

DISPLAYCHAR2:
	CJNE R1, #02H, DISPLAYCHAR1
	MOV DPTR, #CHAR2
	JMP DISPLAY

DISPLAYCHAR1:
	MOV DPTR, #CHAR1
	JMP DISPLAY

DISPLAY:
	MOV A, R2	
	DEC  A
	MOVC A, @A+DPTR
	MOV P1, A
	
	MOV A, R2	
	DEC  A
	MOV DPTR, #DISPLAYSHIFTER
	MOVC A, @A+DPTR
	MOV P0, A
	ACALL DELAY
	DJNZ R2, DISPLAYCHAR3
	DJNZ R0, DISPLAYINIT
	MOV R0, #03CH
	DJNZ R1,  DISPLAYINIT
	RET

DELAY:
	MOV TH0, #0F2H
	MOV TL0, #0CBH
	SETB TR0	
	JNB TF0, $
	CLR TR0
	CLR TF0	
	RET
	END 
	ORG 00H
	AJMP INIT
	ORG 50H

SEG_DATA:
	DB 03H
	DB 09FH
	DB 025H
	DB 0DH
	DB 099H
	DB 049H
	DB 041H
	DB 01FH
	DB 01H
	DB 09H

INIT:
	MOV TMOD, #01H 
	MOV DPTR,  #SEG_DATA

RESET:
	MOV R0, #0AH

LOOP:
	MOV    A,  R0
	DEC  	   A
	MOVC  A,  @A+DPTR
	MOV   P1, A
	MOV   R6, #4H
	MOV   R7, #0FAH
	ACALL DELAYSEC
	DJNZ   R0, LOOP
	JMP RESET

DELAYSEC:
	MOV TH0, #0FCH
	MOV TL0, #018H
	SETB TR0	

WAIT:
	JNB TF0, $
	CLR TR0
	CLR TF0
	DJNZ R6, DELAYSEC
	MOV R6, #4H
	DJNZ R7, DELAYSEC
	RET
	END

	ORG   00H
	AJMP  MAIN
	ORG   50H
MAIN:
	MOV TMOD,#01H 
	MOV   A, #7FH
	
LOOP:
	MOV   P1, A
	RR      A
	MOV   R6, #4H
	MOV   R7, #0FAH
	ACALL DELAYSEC
	JMP LOOP

DELAYSEC:
	MOV TH0, #0FCH
	MOV TL0, #018H
	SETB TR0	
WAIT:
	JNB TF0, $
	CLR TR0
	CLR TF0
	DJNZ R6, DELAYSEC
	MOV R6, #4H
	DJNZ R7, DELAYSEC
	RET
	
	END
	ORG   00H
	AJMP  MAIN
	ORG   50H
MAIN:
	MOV TMOD,#01H 
	MOV   A, #0FFH
	MOV   R0, #07FH
	MOV   R1, #0FEH
	MOV   R2, #24H
	
LOOP:
	MOV   P0, R2
	MOV   P1, A

	MOV   A, R2
	RR      A
	MOV   R2, A

	MOV   A, R0
	RR      A
	MOV   R0, A

	MOV   A, R1
	RL      A
	MOV   R1, A

	MOV   A, R0
	ANL    A, R1

	MOV   R6, #4H
	MOV   R7, #0FAH
	ACALL DELAYSEC
	JMP LOOP

DELAYSEC:
	MOV TH0, #0FCH
	MOV TL0, #018H
	SETB TR0	
WAIT:
	JNB TF0, $
	CLR TR0
	CLR TF0
	DJNZ R6, DELAYSEC
	MOV R6, #4H
	DJNZ R7, DELAYSEC
	RET
	
	END
	ORG   00H
	AJMP  MAIN
	ORG   50H
MAIN:
	MOV TMOD,#01H 
	MOV   A, #00H
	MOV   R0, #01H
	MOV   R1, #0FFH
	MOV   P0, #0FFH
	
LOOP:
	CPL   A
	MOV   P1, A
	MOV   A, R0
	RL      A
	MOV   R0, A

	MOV   R1, P0
	MOV   A, R0
	ANL   A, R1

	MOV   R6, #4H
	MOV   R7, #0FAH
	ACALL DELAYSEC
	JMP LOOP

DELAYSEC:
	MOV TH0, #0FCH
	MOV TL0, #018H
	SETB TR0	
WAIT:
	JNB TF0, $
	CLR TR0
	CLR TF0
	DJNZ R6, DELAYSEC
	MOV R6, #4H
	DJNZ R7, DELAYSEC
	RET
	
	END

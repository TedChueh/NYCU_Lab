	ORG 00H
	AJMP INIT
	ORG 50H

SEG_MYID:
	DB 0FFH 
	DB 099H
	DB 01H
	DB 03H 
	DB 09EH
	DB 09FH
	DB 049H
	DB 0DH
	DB 09EH
	DB 09FH


INIT:
	MOV P0,    #0FFH
	MOV TMOD,  #01H 
	MOV DPTR,  #SEG_MYID

RESET:
	MOV R0, #0AH

LOOP:
	MOV   A, R0
	DEC  	   A
	MOVC  A,  @A+DPTR
	MOV   P1, A
	MOV   R6, #4H
	JB    P0.0, FAST
	MOV   R7, #0FAH
	ACALL DELAYSEC
	DJNZ   R0, LOOP
	JMP RESET

FAST:
	MOV   R7, #03EH
	ACALL DELAYSEC
	DJNZ   R0, LOOP
	JMP RESET

DELAYSEC:
	MOV TH0, #0FCH
	MOV TL0, #018H
	SETB TR0	

WAIT:
	JNB TF0, $
	CLR TR0
	CLR TF0
	DJNZ R6, DELAYSEC
	MOV R6, #4H
	DJNZ R7, DELAYSEC
	RET
	END
